import { existsSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const root = process.cwd();
const reportsDir = join(root, "tests/performance/artillery/reports");
const outPath = join(root, "lib/data/artillery-results.generated.ts");

/**
 * @param {string} dir
 * @returns {string | null}
 */
function latestReportPath(dir) {
  if (!existsSync(dir)) {
    return null;
  }

  const files = readdirSync(dir)
    .filter((name) => name.endsWith(".json"))
    .map((name) => join(dir, name))
    .sort((a, b) => {
      const aStat = readFileSync(a, "utf8").length;
      const bStat = readFileSync(b, "utf8").length;
      // Prefer newest by filename timestamp prefix if present
      return b.localeCompare(a) || bStat - aStat;
    });

  return files[0] ?? null;
}

/**
 * @param {Record<string, unknown>} summary
 */
function pickSummary(summary) {
  if (!summary || typeof summary !== "object") {
    return null;
  }

  return {
    min: Number(summary.min ?? 0),
    max: Number(summary.max ?? 0),
    mean: Number(summary.mean ?? 0),
    median: Number(summary.median ?? 0),
    p95: Number(summary.p95 ?? 0),
    p99: Number(summary.p99 ?? 0),
  };
}

/**
 * @param {string} metricKey
 */
function metricLabel(metricKey) {
  const match = metricKey.match(/^browser\.page\.(LCP|FCP)\.(.+)$/);
  if (!match) {
    return metricKey;
  }

  const [, vital, url] = match;
  let path = url;
  try {
    path = new URL(url).pathname || "/";
  } catch {
    // keep raw
  }

  return `${vital} ${path}`;
}

/**
 * @param {unknown} report
 */
function extractResults(report) {
  const aggregate =
    report && typeof report === "object" && "aggregate" in report
      ? /** @type {Record<string, unknown>} */ (report.aggregate)
      : /** @type {Record<string, unknown>} */ (report);

  const counters = /** @type {Record<string, number>} */ (aggregate.counters ?? {});
  const summaries = /** @type {Record<string, Record<string, number>>} */ (
    aggregate.summaries ?? {}
  );
  const rates = /** @type {Record<string, number>} */ (aggregate.rates ?? {});

  const created =
    counters["vusers.created"] ?? counters["vusers.created.total"] ?? 0;
  const completed = counters["vusers.completed"] ?? 0;
  const failed = counters["vusers.failed"] ?? 0;

  const sessionLength = pickSummary(summaries["vusers.session_length"]);

  /** @type {{ name: string; metric: string; mean: number; p95: number; median: number }[]} */
  const webVitals = [];

  for (const [key, summary] of Object.entries(summaries)) {
    if (!key.startsWith("browser.page.LCP.") && !key.startsWith("browser.page.FCP.")) {
      continue;
    }
    const picked = pickSummary(summary);
    if (!picked) continue;
    webVitals.push({
      name: metricLabel(key),
      metric: key,
      mean: Math.round(picked.mean),
      p95: Math.round(picked.p95),
      median: Math.round(picked.median),
    });
  }

  webVitals.sort((a, b) => a.name.localeCompare(b.name));

  const scenarioNames = Object.keys(counters)
    .filter((key) => key.startsWith("vusers.created_by_name."))
    .map((key) => key.replace("vusers.created_by_name.", ""));

  return {
    generatedAt: new Date().toISOString(),
    sourceReport: null,
    dataSource: "live",
    vusersCreated: created,
    vusersCompleted: completed,
    vusersFailed: failed,
    sessionLengthMs: sessionLength
      ? {
          mean: Math.round(sessionLength.mean),
          median: Math.round(sessionLength.median),
          p95: Math.round(sessionLength.p95),
        }
      : null,
    httpRequestRate: rates["http.request_rate"] ?? null,
    scenarios: scenarioNames.length
      ? scenarioNames
      : ["homeBrowse", "playgroundLogin", "labPageLoad"],
    webVitals,
  };
}

const reportPath = process.argv[2] ? join(root, process.argv[2]) : latestReportPath(reportsDir);

if (!reportPath || !existsSync(reportPath)) {
  console.error(
    "No Artillery report JSON found. Run: npm run test:perf  (with server on :3000)",
  );
  process.exit(1);
}

const raw = JSON.parse(readFileSync(reportPath, "utf8"));
const results = extractResults(raw);
results.sourceReport = reportPath.replace(`${root}/`, "");

const contents = `/** Auto-generated by scripts/generate-artillery-results.mjs — do not edit manually */
import type { DataSource } from "@/lib/data/data-source";

export interface ArtillerySessionLength {
  mean: number;
  median: number;
  p95: number;
}

export interface ArtilleryWebVital {
  name: string;
  metric: string;
  mean: number;
  p95: number;
  median: number;
}

export interface ArtilleryResults {
  generatedAt: string;
  sourceReport: string | null;
  dataSource: DataSource;
  vusersCreated: number;
  vusersCompleted: number;
  vusersFailed: number;
  sessionLengthMs: ArtillerySessionLength | null;
  httpRequestRate: number | null;
  scenarios: string[];
  webVitals: ArtilleryWebVital[];
}

export const artilleryResults: ArtilleryResults = ${JSON.stringify(results, null, 2)};
`;

writeFileSync(outPath, contents, "utf8");
console.log(`Wrote Artillery results to ${outPath}`);
console.log(`Source: ${results.sourceReport}`);
console.log(
  JSON.stringify(
    {
      vusersCreated: results.vusersCreated,
      vusersCompleted: results.vusersCompleted,
      vusersFailed: results.vusersFailed,
      webVitals: results.webVitals.length,
    },
    null,
    2,
  ),
);
